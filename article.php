    			<h1 class="art-title">Bienvenida del Rector</h1>
    			<div class="article">
    				<p>Bienvenidos al Tecnológico de Monterrey. Desde el 12 de septiembre de 2011 tengo el privilegio de liderar y servir a esta gran institución. Como Presidente del Tecnológico de Monterrey me he propuesto trabajar en tres avenidas:</p>

				    <p>Transformación organizacional. Invertir la pirámide para que los alumnos y los profesores sean el centro de nuestro quehacer. Motivar un cambio de cultura para que el resto del personal, quienes no somos ni alumnos ni profesores, tengamos siempre como misión apoyarlos y servirlos. Además aprovechar todo el talento que hay en la institución, con cohesión y alineación de esfuerzos, para asegurar que todas las actividades que se realicen estén orientadas a generar valor agregado para nuestros alumnos, así como para que nuestros profesores puedan cumplir su labor con excelencia.</p>
				     
				    <p>Sentido Humano y orientación a servir. A lo largo de toda la organización, empezando con el equipo de soporte, habremos de incorporar el espíritu de servicio orientado a la formación de nuestros alumnos. A ellos, a nuestros alumnos, seguiremos inculcándoles la formación ética y ciudadana, sustentada sólidamente en los valores fundamentales, así como la responsabilidad social y el humanismo empresarial. Reforzaremos la capacitación en formación ética y ciudadana para nuestros profesores, así como la incorporación de temas éticos y ciudadanos a lo largo del currículo.</p>

				     
				    <p>Reinvención de nosotros mismos. La dinámica del mundo nos obliga a reinventarnos. La innovación como premisa fundamental para poder tener derecho a formar parte del futuro, estimulando la creatividad y reconociendo que el costo de la innovación es equivocarse. Hay que atreverse. La reinvención empieza por conocer mejor las necesidades de nuestros alumnos, de los empleadores, de los padres de familia, del país, para así poder replantear nuestros programas y nuestras propuesta de valor como universidad.</p>
				     

					<p>En el Tecnológico de Monterrey creemos en México y en nuestras comunidades, creemos en nuestra gente, en la diversidad cultural que nos enriquece, en nuestra historia, pero sobre todo, estamos convencidos de que hay que participar activamente en la construcción de nuestro futuro. Sabemos que podemos contribuir a tener un mejor presente y forjarnos el futuro que soñamos. Está en las manos de cada uno de nosotros decidir cómo queremos que sea y, juntos, hacer que suceda. El primer paso para avanzar en este camino es el conocimiento, conocimiento que genera cambios, conocimiento que transforma.</p>
					 

					<p>Los invito a conocer el Tecnológico de Monterrey.</p>

					<h2>Salvador Alva</h2>
